    private void cargarListenerModeloTabla(){
        //Se crea el listener que modificará los datos del total
        modeloTablaProd.addTableModelListener(new TableModelListener(){
            @Override
            //Se carga el método abstracto para poder realizar dicha función
            public void tableChanged(TableModelEvent evt){
                int numFilas = modeloTablaProd.getRowCount();
                double sumatoria = 0;
                for (int i = 0; i < numFilas; i++){
                    String importe = (String)modeloTablaProd.getValueAt(i, 4);
                    sumatoria += Double.parseDouble(importe);
                }
                lblTotalPagar.setText(String.valueOf(sumatoria));
            }
        });
    }//Fin del método que carga el total

