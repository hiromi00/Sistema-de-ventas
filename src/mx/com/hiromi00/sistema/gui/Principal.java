    //Crea un evento en el boton para que muestre la ventana de ventas
    VentasFrame ventanaVentas;
    private void botonVentasActionPerformed(java.awt.event.ActionEvent evt) {                                            
        //Se verifica si existe o no una ventana de ventas
        if (ventanaVentas == null){
            //En caso de no existir, se crear√° una nueva
            ventanaVentas = new VentasFrame();
            fondoPrincipal.add(ventanaVentas);
            fondoPrincipal.getDesktopManager().maximizeFrame(ventanaVentas);
            ventanaVentas.setVisible(true);
        }
        else{
            fondoPrincipal.getDesktopManager().maximizeFrame(ventanaVentas);
        }
        
    }                                           
