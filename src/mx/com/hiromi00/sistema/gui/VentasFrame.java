    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        
        ArrayList<DetalleVenta> detalles = new ArrayList<>();
        //Se obtiene la sumatoria de las ventas
        String sumatoriaVentas = lblTotalPagar.getText();
        double montoVenta = Double.parseDouble(sumatoriaVentas);
        
        //Se realiza el cambio y efectua el pago
        String pagarStr = tfTipoPago.getText();
        double cambio = 0;
        //Se realiza una condicional que dará el cambio
        if (!pagarStr.isEmpty()){
            double pagar = Double.parseDouble(pagarStr);
            cambio = pagar - montoVenta;
        }
        //Se obtiene la fecha actual junto a la hora
        Calendar calendarioLocal = Calendar.getInstance();
        java.util.Date fechaActual = calendarioLocal.getTime();
        
        //Se crean los milisegundos para dar una fecha precisa y es und ato fundamental
        long fechaMiliseg = fechaActual.getTime();
        //Se usa la libreria de DATE DE SQL
        java.sql.Date fecha = new java.sql.Date(fechaMiliseg);
        
        Venta venta = new Venta(montoVenta,fecha);

        //Se agrega la venta a la base de datos
        Long idVenta = database.insertarVenta(venta);
        
        
        int numRows = modeloTablaProd.getRowCount();
        
        for(int i = 0; i < numRows; i++){
            String idPorducto = (String)modeloTablaProd.getValueAt(i, 0);
            String cantidadStr = (String)modeloTablaProd.getValueAt(i, 3);
            double cantidad = Double.parseDouble(cantidadStr);
            
            DetalleVenta detalle = new DetalleVenta(idVenta, idPorducto, cantidad);
            
            database.insertarDetalleVenta(detalle);
            //Se añaden todos los detalles al arraylist
            detalles.add(detalle);
        }
        //Se borran las filas de la tabla
        for(int i = numRows-1; i >=0; i--){
            modeloTablaProd.removeRow(i);
        }
        lblTotalPagar.setText("0.00");
        
        if(!pagarStr.isEmpty()){
            //Se establece un emsnaje en HTML para hacerlo más estetico
            JOptionPane.showMessageDialog(this, "<html><h1 style = 'font-size: 150 px; color: red'>" + cambio + "</h1></html>", "Usted debe de devolver como cambio: ", 1);
            tfTipoPago.setText("");
        }
    }//GEN-LAST:event_btnVenderActionPerformed

    //Despliega la foto a mostrar en la interfaz
    private void desplegarFoto(Producto prod){
        ImageIcon imagenProd = null;
        try{
            //Obitnee la imagen desde la base de datos
            InputStream isFoto = database.buscarFoto(prod);
            BufferedImage biFoto = ImageIO.read(isFoto);
            imagenProd = new ImageIcon(biFoto);
            Image imageProd = imagenProd.getImage();
            int anchoImg = lblimagen.getWidth();
            int altoImg = lblimagen.getHeight();
            
            Image imgRedimensionada = imageProd.getScaledInstance(anchoImg, altoImg, Image.SCALE_DEFAULT);
            
            ImageIcon iconoRedimensionadao = new ImageIcon(imgRedimensionada);

            lblimagen.setIcon(iconoRedimensionadao);
        }catch(IOException ex){
            ex.printStackTrace();
        }
    }
    
    private void tfBuscarProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfBuscarProdKeyReleased
        limpiarListaProductos();
        
        //Comenza la captura de lo introducido en el area "buscar"
        String cadenaBusqueda = tfBuscarProd.getText();
        if (cadenaBusqueda.isEmpty()){
            limpiarListaProductos();
        }
        else{
            ArrayList<Producto> listaProd = database.obtenerProductosPorCriterio(cadenaBusqueda);
        
            for(Producto prod : listaProd){
                modeloListaProd.addElement(prod);
            }
        }
        
    }//GEN-LAST:event_tfBuscarProdKeyReleased

    private void listProdMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listProdMousePressed
        //Obtiene el tipo de fuente de dodne se quiere seleccionar el evento
        JList lista = (JList)evt.getSource();
        if(evt.getClickCount() == 2){
            //Obtiene el indice y el producto que se ha seleccionado
            int index = lista.locationToIndex(evt.getPoint());
            Producto prod = (Producto)lista.getSelectedValue();
            //Se añaden los productos a la tabla 
            agregarProductoVenta(prod);
            
            desplegarFoto(prod);
        }
    }//GEN-LAST:event_listProdMousePressed

    private void tablaProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaProdKeyReleased
        //Espera a que se presione la tecla F2
        if (evt.getKeyCode() == KeyEvent.VK_F2){
            int filaSeleccionada = tablaProd.getSelectedRow();
            //Muestra en mensaje pidiendo la cantidad a agregar
            String cantidad = JOptionPane.showInputDialog("Modificar la cantidad: ");
            String precioVenta = (String)modeloTablaProd.getValueAt(filaSeleccionada, 2);
            //Se realiza el importe a pagar haciendo la multiplicación de los objetos
            double importe = Double.parseDouble(cantidad) * Double.parseDouble(precioVenta);
            //Se establece la cantidad escrita
            modeloTablaProd.setValueAt(cantidad, filaSeleccionada, 3);
            //Se establece el importe establecido
            modeloTablaProd.setValueAt(importe, filaSeleccionada, 4);
            
        }
    }//GEN-LAST:event_tablaProdKeyReleased

    private void agregarProductoVenta(Producto prod){
        String claveProd = prod.getIdProducto();
        String nombreProd = prod.getNomProducto();
        String precioVenta = String.valueOf(prod.getPrecioVentaProducto());
        String importe = String.valueOf(prod.getPrecioVentaProducto());
        
        
        //Se crea un arreglo que sostenga todos los datos que se mostrarán en la tabla
        String [] datosProducto = {claveProd, nombreProd, precioVenta, "1", importe};
        //Se le agrega el producto desde el modelo diseñado anteriormente
        modeloTablaProd.addRow(datosProducto);
    }
    //Método que limpia los datos de la lista de articulos
    private void limpiarListaProductos(){
        modeloListaProd.clear();
    }//Fin del método de limpia lista productos

